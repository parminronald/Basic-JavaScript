/* Operator Aritmateka
+   Penambahan
-   Pengurangan
*   Perkalian
**  Pangkat / Exponansial
/   Pembagian
%   Sisa bagi/modulus
*/
let resoult = 1 + 2; //hasil 3
console.log(`Hasil: 1 + 2 = ${resoult}`);

let hasilResoult;
resoult = resoult * 2; //hasil 3 * 2 = 6
console.log("Hasil" + hasilResoult + " * 2 =" + resoult);

resoult /= 2;
console.log(resoult);
resoult %= 4; // sisa hasil bagi
console.log(resoult);
resoult **= 3; // hasil pangkat
console.log(resoult);
